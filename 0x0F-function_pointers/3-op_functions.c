#include "3-calc.h"

/**
*op_add - returns the sum of a and b
*@a:first argument
*@b:second argument
*Return:sum of a and b
*/


int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - returns the differece of a and b
*@a:first argument
*@b:second argument
*Return:differece of a and b
*/


int op_sub(int a, int b)

{
	return (a - b);
}
/**
*op_mul - returns the multiplication of a and b
*@a:first argument
*@b:second argument
*Return:multiplication of a and b
*/

int op_mul(int a, int b)

{
	return (a * b);
}
/**
*op_div -returns the division of a and b
*@a:first argument
*@b:second argument
*Return:division of a and b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
*op_mod - returns the modulo of a and b
*@a:first argument
*@b:second argument
*Return:modulo of a and b
*/


int op_mod(int a, int b)
{
	return (a % b);
}
